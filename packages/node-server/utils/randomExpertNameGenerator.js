const randomExpertNameGenerator = (withNumber = false) => {
    const animal = animals[Math.floor(Math.random() * animals.length)];
    const adjective = adjectives[Math.floor(Math.random() * adjectives.length)];
    const lowerCaseName = `${adjective}-${animal}`.toLowerCase();
    if (!withNumber) return lowerCaseName;
    const number = withNumber ? Math.floor(Math.random() * 100) : '';
    return `${lowerCaseName}-${number}`;
}

